"""4.Write a Python function to get the largest number, smallest num and sum 
of all from a list."""

list1 = [10,20,30,40,50,60]

max_num= max(list1)
min_num= min(list1)

print("max number is : ",max_num) # largest number
print("min number is : ",min_num) # smallest number
print("sum : ",sum(list1)) # sum